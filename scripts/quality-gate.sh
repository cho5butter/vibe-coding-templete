#!/usr/bin/env bash
set -euo pipefail

# =============================================================================
# 品質ゲート: リント → ビルド → テスト を順番に実行
# すべてパスしないとコミット不可
# =============================================================================

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"

echo "========================================"
echo "  品質ゲート開始"
echo "========================================"

echo ""
echo "[1/3] リント・フォーマット"
bash "$SCRIPT_DIR/lint.sh"

echo ""
echo "[2/3] ビルド確認"
bash "$SCRIPT_DIR/build.sh"

echo ""
echo "[3/3] テスト実行"
bash "$SCRIPT_DIR/test.sh"

echo ""
echo "========================================"
echo "  品質ゲート: すべてパス ✓"
echo "========================================"
